package com.z2i.epsilon.Model;

public class Upload {

    public String title;
    public String domain;
    public String date;
    public String time;
    public String status;
    public String desc;
    public String uid;

    public Upload() {
    }

    public Upload(String title, String domain, String date, String time, String status, String desc, String uid) {
        this.title = title;
        this.domain = domain;
        this.date = date;
        this.time = time;
        this.status = status;
        this.desc = desc;
        this. uid = uid;
    }

}
